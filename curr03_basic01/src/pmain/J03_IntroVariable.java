public class J03_IntroVariable {
	public static void main(String[] args) {
		
		System.out.println("[합격 필요 점수 환산 프로그램]");
		System.out.println("내 현재 점수 : " + 76);
		
		System.out.println("마법학교 합격 시 필요 점수(98) : " + (98-76));
		System.out.println("영웅학교 합격 시 필요 점수(92) : " + (92-76));
		System.out.println("게임학교 합격 시 필요 점수(88) : " + (88-76));
		System.out.println("IT학교 합격 시 필요 점수(85) : " + (85-76));
	}
}
